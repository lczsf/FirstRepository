package com.wdk.wms.basic.daoobject;

/**
 * Description: 字典表 key 和 value 对应
 *
 * @author 刘勇
 * @Date 15/9/9
 */
public class DictDO {

	// 键
	private String key;

	// 值
	private String value;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
